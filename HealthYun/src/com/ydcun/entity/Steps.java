package com.ydcun.entity;

public class Steps{
	private Long t;
	private Long d;
	private Long c;
	private Long p;
	private Long s;
	private Long j;
	private Long w;
	
	public Steps() {
		super();
	}
	
	public Steps(Long t, Long d, Long c, Long p, Long s, Long j, Long w) {
		super();
		this.t = t;
		this.d = d;
		this.c = c;
		this.p = p;
		this.s = s;
		this.j = j;
		this.w = w;
	}

	public Long getT() {
		return t;
	}
	public void setT(Long t) {
		this.t = t;
	}
	public Long getD() {
		return d;
	}
	public void setD(Long d) {
		this.d = d;
	}
	public Long getC() {
		return c;
	}
	public void setC(Long c) {
		this.c = c;
	}
	public Long getP() {
		return p;
	}
	public void setP(Long p) {
		this.p = p;
	}
	public Long getS() {
		return s;
	}
	public void setS(Long s) {
		this.s = s;
	}
	public Long getJ() {
		return j;
	}
	public void setJ(Long j) {
		this.j = j;
	}
	public Long getW() {
		return w;
	}
	public void setW(Long w) {
		this.w = w;
	}
	
}
